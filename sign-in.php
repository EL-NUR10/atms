// If user cllicks 'Sign In' button
if (isset($_POST["submit"])) {
    $email_matric = $_POST["email_matric"];
    $password = $_POST["password"];

    // Selecting Email From `lecturer` Table
    $select_email = mysqli_query($connect,"SELECT * FROM `lecturer` WHERE email = '$email_matric'");


    // Selecting Matric From `student` Table
    $select_matric = mysqli_query($connect,"SELECT * FROM `student` WHERE matricno = '$email_matric'");


    // Checking if Credential is correct
    #checking if email or matric is correct
    if (mysqli_num_rows($select_email) > 0 || mysqli_num_rows($select_matric) > 0) {
        #checking if email is correct
        if (mysqli_num_rows($select_email) > 0) {
            // Fetching Password for Lecturer
          $fetch_email = mysqli_fetch_assoc($select_email);  
          $db_password = $fetch_email["password"];
          
          if (!password_verify($password,$db_password)) {
            $password_err = "Password incorrect";
          }else {
            $_SESSION["lecturer_id"] = $fetch_email["lecturer_id"];
            header("location:lecturer/dashboard.php");
          }
        }

        #checking if matric is correct
        if (mysqli_num_rows($select_matric) > 0) {
            // Fetching Password for Student
          $fetch_matric = mysqli_fetch_assoc($select_matric);  
          $db_password = $fetch_matric["password"];
          
          if (!password_verify($password,$db_password)) {
            $password_err = "Password incorrect";
          }else {
            $_SESSION["student_id"] = $fetch_matric["student_id"];
            header("location:student/dashboard.php");
          }
        }

    }else {
        $email_matric_err = "Email or Matric not correct";
    }
